#include <stdio.h>
#include <stdlib.h>

int main()
{

    printf("Characters: %c %c \n", 'a', 65);
    printf("Decimal Numbers: %d \n", 1977);
    printf("Space Before: %10d \n", 1977);
    printf("Zero Before: %010d \n", 1977);
    printf("Float Numbers: %.2f \n", 3.1416);
    printf("%s \n", "One string");


    return 0;
}
